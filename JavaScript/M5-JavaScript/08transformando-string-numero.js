// TRANSFORMANDO STRING EM NUMEROS
var a = 3;
var b = 5;
var c = a + b;

console.log(c); // 8
//--------------------------//

var a = '3';
var b = '5';
var c = a + b;

console.log(c); // 35
//--------------------------//

var a = 3;
var b = 5;
var c = parseInt(a) + parseInt(b); // numero inteiro;

console.log(c);  // 8
//--------------------------//

var a = 3.5;
var b = 5;
var c = parseFloat(a) + parseFloat(b);  // numero decimal;

console.log(c);  // 8.5
