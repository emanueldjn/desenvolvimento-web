// Subtração -
// Multiplicação * 
// Divisão /
// Módulo %
// Incremento  ++
// Decremento --



var a = 10;
var b = 15;
var c = a + b;
console.log(c); // 25;

// Incremento  ++
var a = 30;
a++;
console.log(a) // 31

var a = 30;
b = ++a;
console.log(a); // 31 (Primeiro incrementa o A, depois pega o valor final e coloca em B);
