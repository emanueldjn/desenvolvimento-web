// Operadores são usados para realizar operações em variáveis e valores. 

let x = 10;
let y = 5;

console.log(x + y); // Adição: 15
console.log(x - y); // Subtração: 5
console.log(x * y); // Multiplicação: 50
console.log(x / y); // Divisão: 2
console.log(x % y); // Módulo: 0
console.log(x === y); // Igualdade estrita: false
console.log(x !== y); // Desigualdade estrita: true
console.log(x > y); // Maior que: true
console.log(x < y); // Menor que: false